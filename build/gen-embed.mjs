#!/usr/bin/env node
/**
 * Generate the embed barrel + entry shim for a `bun build --compile` binary.
 *
 * Reads the CLI's `assets` block from `build/cli-manifest.json`, inlines every
 * matching asset file's text via `import ... with { type: "text" }`, and emits:
 *
 *   <outDir>/<cli>.assets.mjs  — text imports + registerAssets() calls
 *   <outDir>/<cli>.entry.mjs   — imports the barrel, then the real entry
 *
 * The barrel runs before the entry, so the asset registry is populated before
 * the entry's module body executes. Prints the path to compile (the shim when
 * the CLI declares assets, otherwise the original entry unchanged) to stdout so
 * `just build-binary` can feed it straight to `bun build --compile`.
 *
 * Asset selection: every file under each `from` directory except code modules
 * (.js/.mjs/.cjs/.ts/.tsx/.jsx) — those are part of the JS module graph and are
 * bundled by Bun already. An asset entry may narrow this with an `ext` array.
 *
 * Usage: node build/gen-embed.mjs <cliName> <entryAbsPath> <outDir>
 */

import { readFileSync, readdirSync, mkdirSync, writeFileSync } from "node:fs";
import { join, relative, resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const [cliName, entryPath, outDir] = process.argv.slice(2);

if (!cliName || !entryPath || !outDir) {
  console.error("usage: gen-embed.mjs <cliName> <entryAbsPath> <outDir>");
  process.exit(1);
}

const CODE_EXT = new Set([".js", ".mjs", ".cjs", ".ts", ".tsx", ".jsx"]);

const manifest = JSON.parse(
  readFileSync(join(repoRoot, "build", "cli-manifest.json"), "utf-8"),
);
const cli = manifest.clis.find((c) => c.name === cliName);
const assets = cli?.assets ?? [];

// No declared assets → compile the entry as-is. Print it unchanged.
if (assets.length === 0) {
  process.stdout.write(entryPath);
  process.exit(0);
}

/** Recursively list files under `dir` relative to it (posix separators). */
function listFiles(dir, base = dir) {
  const out = [];
  for (const e of readdirSync(dir, { withFileTypes: true })) {
    const abs = join(dir, e.name);
    if (e.isDirectory()) out.push(...listFiles(abs, base));
    else out.push(relative(base, abs).split("\\").join("/"));
  }
  return out;
}

const importLines = [];
const mountBlocks = [];
let idx = 0;

for (const asset of assets) {
  const fromAbs = resolve(repoRoot, asset.from);
  const extFilter = asset.ext ? new Set(asset.ext) : null;
  const rels = listFiles(fromAbs).filter((rel) => {
    const dot = rel.lastIndexOf(".");
    const ext = dot === -1 ? "" : rel.slice(dot);
    if (extFilter) return extFilter.has(ext);
    return !CODE_EXT.has(ext);
  });
  if (rels.length === 0) {
    console.error(`gen-embed: no assets matched under ${asset.from}`);
    process.exit(1);
  }
  const entries = rels.map((rel) => {
    const name = `a${idx++}`;
    const abs = join(fromAbs, rel);
    importLines.push(
      `import ${name} from ${JSON.stringify(abs)} with { type: "text" };`,
    );
    return `  ${JSON.stringify(rel)}: ${name},`;
  });
  mountBlocks.push(
    `registerAssets(${JSON.stringify(asset.mount)}, {\n${entries.join("\n")}\n});`,
  );
}

// Absolute so the emitted `import` statements resolve as files, not bare
// package specifiers, regardless of where `bun build` runs from.
const outAbs = resolve(repoRoot, outDir);
mkdirSync(outAbs, { recursive: true });

const barrelPath = join(outAbs, `${cliName}.assets.mjs`);
const shimPath = join(outAbs, `${cliName}.entry.mjs`);

writeFileSync(
  barrelPath,
  `// Generated by build/gen-embed.mjs — do not edit.\n` +
    `import { registerAssets } from "@forwardimpact/libcli";\n` +
    `${importLines.join("\n")}\n\n` +
    `${mountBlocks.join("\n")}\n`,
);

// Absolute so the shim's `import` resolves the entry as a file, not a bare
// package specifier (the manifest hands us a repo-relative entry path).
const entryAbs = resolve(repoRoot, entryPath);

writeFileSync(
  shimPath,
  `// Generated by build/gen-embed.mjs — do not edit.\n` +
    `import ${JSON.stringify(barrelPath)};\n` +
    `import ${JSON.stringify(entryAbs)};\n`,
);

process.stdout.write(shimPath);
